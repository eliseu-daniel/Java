    private void btnCadLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadLoginActionPerformed
        // TODO add your handling code here:
        String nome, email, senha, telefone;
        
        nome = txtNomeCadLogin.getText();
        email = txtEmailCadLogin.getText();
        senha = txtSenhaCadLogin.getText();
        telefone = txtTelefoneCadLogin.getText();
        
        ControllerDTO objcontrollerdto = new ControllerDTO();
        objcontrollerdto.setNomeUsuario(nome);
        objcontrollerdto.setEmailUsuario(email);
        objcontrollerdto.setSenhaUsuario(senha);
        objcontrollerdto.setTelefoneUsuario(telefone);
        
        ModelDAO objmodeldao = new ModelDAO();
        objmodeldao.cadastrarUsuario(objcontrollerdto);
        
        limparDados();
    }//GEN-LAST:event_btnCadLoginActionPerformed

